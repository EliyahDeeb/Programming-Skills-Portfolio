#Exercise 5: Calculating Area of Circle

#The 'float' data type is similar to the 'integer' data type, it just has a decimal on it.
r=float(input("Enter Number: "))
#The variable named 'r' is the radius of the circle.

#In order to find the area of a circle, the formula must be used in the command (which is 3.14*r*r).
#This is how it's used:
a=3.14*r*r 
print("Area= ",a)