#Exercise 5: Favorite Fruit

#This exercise is about whether certain names in a list are found.
#If a specific name is found in a list, the 'if' command that 
#associates with that name will be executed.

#However, if the name isn't found on the list, 
#then the 'if' command for that certain command won't be executed.

favorite_fruits = ['apple','banana','purple grape']

if 'purple grape' in favorite_fruits:
    print("You really like grapes!")
if 'strawberry' in favorite_fruits:
    print("You really like strawberries!")
if 'cucumber' in favorite_fruits:
    print("You really like cucumbers.. strange.")
if 'apple' in favorite_fruits:
    print("You really like apples!")
if 'black grape' in favorite_fruits:
    print("You really like black grapes!")
    
